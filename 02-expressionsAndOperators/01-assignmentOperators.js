/*
ASSIGNMENT OPERATORS
*/

let r = 5
// our = sign is the assignment operator
console.log(r);

// ADDITION ASSIGNMENT
r += 1; // r = r + 1, or r = 5 + 1 in this case
console.log(r);

// SUBTRACTION ASSIGNMENT
r -= 2; // r = r - 2, or r = 6 - 2 in this case
console.log(r);

// MULTIPLICATION ASSIGNMENT
r *= 2; // r = r * 2, or r = 4 * 2 in this case
console.log(r);

// DIVISION ASSIGNMENT
r /= 1; // r = r / 2, or r = 8 / 1 in this case
console.log(r);

// EXPONENTIAL ASSIGNMENT
r **= 3; // r = r^2, or r = 8 * 8 * 8 in this case
console.log(r);

// REMAINDER (modulus) ASSIGNMENT
r %= 1; // r = r / 1, with 0 remainder after division
console.log(r);

let y = 5;
console.log(y);
y %= 2;
console.log(y);